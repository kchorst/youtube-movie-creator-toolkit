def format_tags(tags_str):
    """
    Convert comma-separated tags string to a list for the API.
    YouTube accepts up to 500 characters total for tags.
    """
    tags = [t.strip() for t in tags_str.split(",") if t.strip()]
    # Enforce YouTube 500 char limit
    result = []
    total  = 0
    for tag in tags:
        if total + len(tag) + 1 <= 500:
            result.append(tag)
            total += len(tag) + 1
    return result


