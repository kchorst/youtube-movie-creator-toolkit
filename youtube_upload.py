def push_metadata_update(youtube_id, title, description, tags,
                         privacy=None, category="19"):
    """
    Push updated metadata to an already-uploaded YouTube video.
    Does NOT re-upload the video — only updates text fields.
    Cost: ~50 units (vs 1,600 for a full upload).

    Args:
        youtube_id:  YouTube video ID (e.g. 'dQw4w9WgXcQ')
        title:       New title (max 100 chars)
        description: New description
        tags:        Comma-separated tags string or list
        privacy:     'public' / 'private' / 'unlisted' or None to leave unchanged
        category:    YouTube category ID (default 19 = Travel & Events)

    Returns:
        True on success, False on failure
    """
    try:
        youtube = authenticate()

        # Convert tags string to list if needed
        if isinstance(tags, str):
            tags_list = [t.strip() for t in tags.split(",") if t.strip()]
        else:
            tags_list = tags

        # Build update body — snippet contains text fields
        body = {
            "id": youtube_id,
            "snippet": {
                "title":       title[:100],
                "description": description,
                "tags":        tags_list,
                "categoryId":  category,
            }
        }

        # Add status update only if privacy change requested
        if privacy:
            body["status"] = {"privacyStatus": privacy}
            parts = "snippet,status"
        else:
            parts = "snippet"

        youtube.videos().update(
            part=parts,
            body=body
        ).execute()

        log(f"  Metadata updated: https://youtu.be/{youtube_id}")
        return True

    except HttpError as e:
        log(f"  ERROR updating metadata for {youtube_id}: {e}")
        return False
    except Exception as e:
        log(f"  ERROR: {e}")
        return False


