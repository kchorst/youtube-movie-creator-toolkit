def get_upload_video(row):
    """
    Choose which video file to upload.
    Priority: video_file_music > video_file_silent
    Returns (path, is_music) or (None, False) if neither exists.
    """
    music  = row.get("video_file_music", "").strip()
    silent = row.get("video_file_silent", "").strip()

    if music and os.path.isfile(music):
        return music, True
    if silent and os.path.isfile(silent):
        return silent, False
    return None, False


