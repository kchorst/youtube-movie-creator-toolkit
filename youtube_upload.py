def mark_uploaded(log_data, folder_name, youtube_id, title):
    """Record a successful upload in the log."""
    log_data[folder_name] = {
        "youtube_id":   youtube_id,
        "youtube_url":  f"https://youtu.be/{youtube_id}",
        "title":        title,
        "uploaded_at":  datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    }
    save_upload_log(log_data)


