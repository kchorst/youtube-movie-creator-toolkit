def fetch_live_metadata(youtube, youtube_id):
    """
    Fetch current title, description, tags, and privacy
    for a video directly from YouTube.
    Returns dict or None on failure.
    """
    try:
        response = youtube.videos().list(
            part="snippet,status",
            id=youtube_id
        ).execute()

        items = response.get("items", [])
        if not items:
            return None

        snippet = items[0].get("snippet", {})
        status  = items[0].get("status", {})

        return {
            "title":       snippet.get("title", ""),
            "description": snippet.get("description", ""),
            "tags":        ", ".join(snippet.get("tags", [])),
            "privacy":     status.get("privacyStatus", ""),
            "category":    snippet.get("categoryId", "19"),
        }

    except HttpError:
        return None


def fetch_all_channel_videos(youtube):
    """
    Fetch all videos on the authenticated channel.
    Returns list of dicts: { youtube_id, title, published_at, privacy }
    Handles pagination.
    """
    videos     = []
    page_token = None

    while True:
        params = {
            "part":       "id,snippet,status",
            "mine":       True,
            "type":       "video",
            "maxResults": 50,
        }
        if page_token:
            params["pageToken"] = page_token

        try:
            response   = youtube.search().list(**params).execute()
        except HttpError as e:
            log(f"  ERROR fetching channel videos: {e}")
            break

        for item in response.get("items", []):
            videos.append({
                "youtube_id":   item["id"]["videoId"],
                "title":        item["snippet"]["title"],
                "published_at": item["snippet"]["publishedAt"][:10],
                "privacy":      item.get("status", {}).get("privacyStatus", ""),
            })

        page_token = response.get("nextPageToken")
        if not page_token:
            break

    return videos


