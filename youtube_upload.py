def load_quota_log():
    """Load quota_log.json keyed by date."""
    path = QUOTA_LOG()
    if not path or not os.path.isfile(path):
        return {}
    try:
        with open(path, "r", encoding="utf-8") as f:
            return json.load(f)
    except Exception:
        return {}


def save_quota_log(data):
    """Save quota log to disk."""
    path = QUOTA_LOG()
    if not path:
        return
    try:
        os.makedirs(os.path.dirname(path), exist_ok=True)
        with open(path, "w", encoding="utf-8") as f:
            json.dump(data, f, indent=2)
    except Exception as e:
        log(f"  WARNING: Could not save quota log: {e}")


def record_quota(units):
    """Add units used to today's quota log entry."""
    today = datetime.now().strftime("%Y-%m-%d")
    data  = load_quota_log()
    if today not in data:
        data[today] = {"units_used": 0}
    data[today]["units_used"] = data[today].get("units_used", 0) + units
    save_quota_log(data)


def show_quota_status():
    """Print today's quota usage."""
    today = datetime.now().strftime("%Y-%m-%d")
    data  = load_quota_log()
    used  = data.get(today, {}).get("units_used", 0)
    limit = 10000
    safe  = (limit - used) // 1650
    print(f"\n  YouTube API quota ({today}):")
    print(f"    Used      : {used:,} / {limit:,} units")
    print(f"    Remaining : {(limit-used):,} units  (~{safe} more videos)\n")


class QuotaTracker:
    """
    Tracks estimated API quota usage for the current session.
    YouTube resets quota daily at midnight Pacific time.
    This is an estimate — actual usage may vary slightly.
    """
    def __init__(self):
        self.used = 0

    def charge(self, units):
        self.used += units

    def remaining(self):
        return DAILY_QUOTA - self.used

    def can_upload(self, with_thumbnail=True):
        cost = UNITS_PER_UPLOAD + (UNITS_PER_THUMBNAIL if with_thumbnail else 0)
        return self.remaining() >= cost + QUOTA_SAFETY_BUFFER

    def report(self):
        return (f"  Quota used this session: ~{self.used} units | "
                f"Remaining: ~{self.remaining()} of {DAILY_QUOTA}")


