def upload_video(youtube, row, dry_run=False):
    """
    Upload a single video to YouTube with all metadata.
    Returns youtube_id on success, None on failure or skip.

    Args:
        youtube:  Authenticated YouTube API service
        row:      CSV row dict with all metadata fields
        dry_run:  If True, print what would happen but don't upload
    """
    folder_name = row.get("folder_name", "").strip()
    title       = row.get("title", "").strip()
    description = row.get("description", "").strip()
    tags_str    = row.get("tags", "").strip()
    category    = row.get("category", "19").strip()
    privacy     = row.get("privacy", "private").strip()
    kids        = row.get("made_for_kids", "yes").strip().lower() == "yes"
    thumbnail   = row.get("thumbnail", "").strip()

    # Get video file
    video_path, is_music = get_upload_video(row)
    if not video_path:
        log(f"  SKIP: No video file found for '{folder_name}'")
        return None

    tags = format_tags(tags_str)

    if dry_run:
        music_tag = "[MUSIC]" if is_music else "[SILENT]"
        print(f"\n  DRY RUN: {folder_name}")
        print(f"  Video    : {os.path.basename(video_path)} {music_tag}")
        print(f"  Title    : {title}")
        print(f"  Privacy  : {privacy}")
        print(f"  Category : {CATEGORY_NAMES.get(category, category)}")
        print(f"  Kids     : {kids}")
        print(f"  Tags     : {len(tags)} tags")
        print(f"  Thumbnail: {os.path.basename(thumbnail) if thumbnail else 'none'}")
        return "DRY_RUN"

    # ── Build request body ────────────────────────────────────
    body = {
        "snippet": {
            "title":       title[:100],     # YouTube title limit
            "description": description,
            "tags":        tags,
            "categoryId":  category,
        },
        "status": {
            "privacyStatus":          privacy,
            "selfDeclaredMadeForKids": kids,
            "license":                row.get("license", "youtube"),
        }
    }

    # ── Upload video ──────────────────────────────────────────
    log(f"  Uploading: {title[:60]}...")
    log(f"  File: {os.path.basename(video_path)}")

    media = MediaFileUpload(
        video_path,
        mimetype="video/mp4",
        resumable=True,
        chunksize=1024 * 1024 * 8   # 8MB chunks
    )

    request = youtube.videos().insert(
        part="snippet,status",
        body=body,
        media_body=media
    )

    # ── Resumable upload with progress ───────────────────────
    youtube_id = None
    response   = None

    while response is None:
        try:
            status, response = request.next_chunk()
            if status:
                pct = int(status.progress() * 100)
                print(f"  Upload progress: {pct}%", end="\r")
        except HttpError as e:
            if e.resp.status in (500, 502, 503, 504):
                # Transient server error — wait and retry
                log(f"  Server error {e.resp.status} — retrying in 10s...")
                time.sleep(10)
            else:
                log(f"  ERROR uploading '{folder_name}': {e}")
                return None

    youtube_id = response.get("id")
    print()   # Clear progress line
    log(f"  Uploaded: https://youtu.be/{youtube_id}")

    # ── Upload thumbnail ──────────────────────────────────────
    if thumbnail and os.path.isfile(thumbnail):
        try:
            log(f"  Setting thumbnail: {os.path.basename(thumbnail)}")
            youtube.thumbnails().set(
                videoId=youtube_id,
                media_body=MediaFileUpload(thumbnail)
            ).execute()
            log("  Thumbnail set.")
        except HttpError as e:
            log(f"  WARNING: Thumbnail upload failed: {e}")
            # Non-fatal — video still uploaded successfully

    return youtube_id


