def get_live_video_metadata(youtube, youtube_id):
    """
    Fetch current title, description, tags, privacy, and category
    for a live video by its YouTube ID.
    Returns dict or None on failure.
    Used by UC6 (View & Edit Live Metadata).
    """
    try:
        response = youtube.videos().list(
            part="snippet,status",
            id=youtube_id
        ).execute()

        items = response.get("items", [])
        if not items:
            return None

        snippet = items[0]["snippet"]
        status  = items[0]["status"]

        return {
            "youtube_id":         youtube_id,
            "title":              snippet.get("title", ""),
            "description":        snippet.get("description", ""),
            "tags":               ", ".join(snippet.get("tags", [])),
            "category":           snippet.get("categoryId", "19"),
            "privacy":            status.get("privacyStatus", "private"),
            "published_at":       snippet.get("publishedAt", "")[:10],
            "made_for_kids":      status.get("selfDeclaredMadeForKids", False),
            "license":            status.get("license", "youtube"),
            "embeddable":         status.get("embeddable", True),
            "public_stats":       status.get("publicStatsViewable", True),
            "default_language":   snippet.get("defaultLanguage", "en"),
            "audio_language":     snippet.get("defaultAudioLanguage", "en"),
        }

    except HttpError as e:
        log(f"  ERROR fetching metadata for {youtube_id}: {e}")
        return None


def push_metadata_update(youtube, youtube_id, title, description, tags_str,
                         privacy=None, category="19",
                         made_for_kids=False, license="youtube",
                         embeddable=True, public_stats=True,
                         default_language="en", audio_language="en",
                         paid_promo=False):
    """
    Push updated title, description, and tags to a live YouTube video.
    Does NOT re-upload the video — only updates metadata.
    Cost: ~50 API units (very cheap vs 1600 for upload).

    Args:
        youtube:     Authenticated YouTube API service
        youtube_id:  YouTube video ID to update
        title:       New title (max 100 chars)
        description: New description
        tags_str:    Comma-separated tags string
        privacy:     Optional — public/private/unlisted. None = keep existing.
        category:    YouTube category ID (default 19 = Travel & Events)

    Returns True on success, False on failure.
    """
    try:
        # First fetch existing snippet to preserve fields we're not changing
        existing = get_live_video_metadata(youtube, youtube_id)
        if not existing:
            log(f"  ERROR: Could not fetch existing metadata for {youtube_id}")
            return False

        tags = [t.strip() for t in tags_str.split(",") if t.strip()]

        # Resolve privacy — use passed value or fall back to existing
        resolved_privacy = privacy if privacy else (existing.get("privacy") or "private")

        body = {
            "id": youtube_id,
            "snippet": {
                "title":                title[:100],
                "description":          description,
                "tags":                 tags,
                "categoryId":           category,
                "defaultLanguage":      default_language,
                "defaultAudioLanguage": audio_language,
            },
            "status": {
                "privacyStatus":              resolved_privacy,
                "selfDeclaredMadeForKids":    made_for_kids,
                "license":                    license,
                "embeddable":                 embeddable,
                "publicStatsViewable":        public_stats,
                "containsSyntheticMedia":     False,
                "madeForKids":                made_for_kids,
            }
        }

        youtube.videos().update(
            part="snippet,status",
            body=body
        ).execute()

        log(f"  Updated: https://youtu.be/{youtube_id}")
        log(f"  Title: {title[:60]}")
        return True

    except HttpError as e:
        log(f"  ERROR pushing metadata for {youtube_id}: {e}")
        return False
