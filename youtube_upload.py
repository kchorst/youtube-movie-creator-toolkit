# MAIN UPLOAD LOOP
# ------------------------------------------------------------

def run_uploads(dry_run=False):
    show_quota_status()
    """
    Main upload loop.
    - Loads CSV and upload log
    - Skips already uploaded and missing video files
    - Stops gracefully when quota is near limit
    - Saves progress after each upload
    """
    rows       = load_csv()
    upload_log = load_upload_log()
    quota      = QuotaTracker()

    if not rows:
        return

    # ── Filter pending rows ───────────────────────────────────
    pending = []
    for row in rows:
        folder_name = row.get("folder_name", "").strip()
        if folder_name in upload_log:
            continue    # Already uploaded
        video_path, _ = get_upload_video(row)
        if not video_path:
            continue    # No video file
        pending.append(row)

    total = len(pending)

    if total == 0:
        print("\n  Nothing to upload — all rows already uploaded or missing video files.")
        return

    print(f"\n  {len(rows)} rows in CSV | {total} pending upload\n")

    if dry_run:
        print("  ── DRY RUN MODE — no actual uploads ──\n")

    # ── Upload each pending row ───────────────────────────────
    youtube    = None if dry_run else authenticate()
    done       = 0
    skipped    = 0
    quota_stop = False

    for i, row in enumerate(pending, 1):
        folder_name  = row.get("folder_name", "").strip()
        title        = row.get("title", "")[:60]
        thumbnail    = row.get("thumbnail", "").strip()
        has_thumb    = bool(thumbnail and os.path.isfile(thumbnail))

        print(f"\n  [{i}/{total}] {folder_name}")

        # Quota check
        if not dry_run and not quota.can_upload(with_thumbnail=has_thumb):
            print(f"\n  QUOTA LIMIT REACHED — stopping for today.")
            print(f"  {quota.report()}")
            print(f"  {total - done - skipped} videos remaining — resume tomorrow.")
            quota_stop = True
            break

        # Upload
        youtube_id = upload_video(youtube, row, dry_run=dry_run)

        if youtube_id and youtube_id != "DRY_RUN":
            mark_uploaded(upload_log, folder_name, youtube_id, title)
            quota.charge(UNITS_PER_UPLOAD + (UNITS_PER_THUMBNAIL if has_thumb else 0))
            done += 1
            time.sleep(UPLOAD_DELAY)
        elif youtube_id == "DRY_RUN":
            done += 1
        else:
            skipped += 1

    # ── Summary ───────────────────────────────────────────────
    print(f"\n{'='*60}")
    if dry_run:
        print(f"  DRY RUN complete. {done} would be uploaded, {skipped} skipped.")
    else:
        print(f"  Upload session complete.")
        print(f"  Uploaded : {done}")
        print(f"  Skipped  : {skipped}")
        if not quota_stop:
            print(f"  All pending videos uploaded.")
        print(f"  {quota.report()}")
    print(f"{'='*60}\n")


